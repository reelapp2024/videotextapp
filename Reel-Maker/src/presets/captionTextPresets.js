export const ALL_CAPTION_TEXT_PRESETS = CAPTION_TEXT_PRESET_CATEGORIES.flatMap((c) => c.presets)

export function getCaptionTextPresetById(id) {
  return ALL_CAPTION_TEXT_PRESETS.find((p) => p.id === id) || null
}

export function applyCaptionTextPresetPatch(overlay, preset) {
  if (!preset?.patch) return overlay
  const { _captionSync, ...overlayPatch } = preset.patch
  return { ...overlay, ...overlayPatch }
}
