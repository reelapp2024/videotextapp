export function buildCaptionTextPreset(name, categoryId, o = {}) {
  const id = o.id || slug(name)
  const isBox = (o.styleType || BASE.styleType || 'stroke') === 'box'
  const patch = {
    ...BASE,
    captionTextPresetId: id,
    styleType: o.styleType ?? 'stroke',
    color: o.color ?? '#FFFFFF',
    strokeColor: o.strokeColor ?? '#000000',
    strokeOpacity: o.strokeOpacity ?? 1,
    strokeWidthPercent: o.strokeWidthPercent ?? 6,
    bgColor: o.bgColor ?? 'rgba(0,0,0,0.82)',
    bgOpacity: o.bgOpacity ?? (isBox ? 0.88 : 0),
    boxPaddingPercent: o.boxPaddingPercent ?? 38,
    boxCornerRadiusPercent: o.boxCornerRadiusPercent ?? 18,
    fontFamily: o.fontFamily ?? 'Montserrat',
    ...o,
    captionTextPresetId: id,
  }
  if (o._captionSync) patch._captionSync = o._captionSync
  return { id, name, categoryId, patch }
}

const cat = (categoryId, label, items) => ({
  id: categoryId,
  label,
  presets: items.map(([n, o]) => buildCaptionTextPreset(n, categoryId, o)),
})

/** Title typography presets — includes readable style label for UI. */
const typographyCat = (categoryId, label, items) => ({
  id: categoryId,
  label,
  presets: items.map(([name, typographyStyle, o]) => {
    const preset = buildCaptionTextPreset(name, categoryId, o)
    return { ...preset, typographyStyle }
  }),
})

